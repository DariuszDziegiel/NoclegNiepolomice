<?php

namespace AdminBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * CmsGalleryCategoryTranslation
 *
 * @ORM\Table(name="cms_gallery_category_translation")
 * @ORM\Entity(repositoryClass="AdminBundle\Repository\CmsGalleryCategoryTranslationRepository")
 */
class CmsGalleryCategoryTranslation
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }
}
